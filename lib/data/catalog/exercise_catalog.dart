  static List<Exercise> all() => _all;

  static List<Exercise> browsable() => _all
      .where((exercise) => SkillCategoryCatalog.isBrowsableId(
            _resolveSkillCategoryId(exercise),
          ))
      .toList();

  static List<Exercise> forCategory(ExerciseCategory category) =>
      _all.where((e) => e.category == category).toList()
        ..sort((a, b) => a.treeOrder.compareTo(b.treeOrder));

  static List<Exercise> forSkillCategory(String skillCategoryId) =>
      _all.where((e) => _resolveSkillCategoryId(e) == skillCategoryId).toList()
        ..sort((a, b) => a.treeOrder.compareTo(b.treeOrder));

  static int totalForCategory(ExerciseCategory category) =>
      _all.where((e) => e.category == category).length;

  static int totalForSkillCategory(String skillCategoryId) =>
      _all.where((e) => _resolveSkillCategoryId(e) == skillCategoryId).length;

  static Exercise? findById(String id) {
    for (final exercise in _all) {
      if (exercise.id == id) return exercise;
    }
    return null;
  }

  static String skillCategoryIdForExercise(Exercise exercise) =>
      _resolveSkillCategoryId(exercise);

  static String _resolveSkillCategoryId(Exercise exercise) =>
      exercise.skillCategoryId.isEmpty
          ? exercise.category.id
          : exercise.skillCategoryId;
